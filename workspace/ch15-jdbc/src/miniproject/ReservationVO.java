package miniproject;

public class ReservationVO {
	private int re_num;
	private int me_num;
	private int mo_num;
	private int re_seats;
	
	public int getRe_num() {
		return re_num;
	}
	public void setRe_num(int re_num) {
		this.re_num = re_num;
	}
	public int getMe_num() {
		return me_num;
	}
	public void setMe_num(int me_num) {
		this.me_num = me_num;
	}
	public int getMo_num() {
		return mo_num;
	}
	public void setMo_num(int mo_num) {
		this.mo_num = mo_num;
	}
	public int getRe_seats() {
		return re_seats;
	}
	public void setRe_seats(int re_seats) {
		this.re_seats = re_seats;
	}
	
	

}