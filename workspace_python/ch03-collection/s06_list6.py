data = [20,10,50,40,30]
print(data)
print('-'*3)

# 반대로 정렬
data.reverse()
print(data)
print('-'*3)

# 오름차순으로 정렬
data.sort()
print(data)
print('-'*3)

# 내림차순으로 정렬
data.sort(reverse=True)
print(data);
print('-'*3)

